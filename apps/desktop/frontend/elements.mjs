export const REQUIRED_ELEMENT_LIST_SELECTORS = Object.freeze({
  navigation: "[data-workflow]",
  panels: "[data-panel]",
  conductorRefreshButtons: "[data-conductor-refresh]",
  lifecycleButtons: "[data-conductor-action]",
});

export function collectDesktopElements(root) {
  const elements = {};
  for (const [name, selector] of Object.entries(REQUIRED_ELEMENT_SELECTORS)) {
    const element = root.querySelector(selector);
    if (!element) throw new Error(`Missing required desktop element ${name}: ${selector}`);
    elements[name] = element;
  }
  for (const [name, selector] of Object.entries(REQUIRED_ELEMENT_LIST_SELECTORS)) {
    const matches = [...root.querySelectorAll(selector)];
    if (matches.length === 0) throw new Error(`Missing required desktop element list ${name}: ${selector}`);
    elements[name] = matches;
  }
  return elements;
}
